package org.openhab.binding.velux.internal.config;

import org.eclipse.smarthome.config.core.Configuration;
import org.openhab.binding.velux.VeluxBindingProperties;

/**
 * The {@link VeluxThingConfiguration} is a wrapper for
 * configuration settings needed to access the <B>Velux</B> device to be used for method
 * {@link org.openhab.binding.velux.handler.VeluxHandler#initialize()}.
 * <p>
 * It contains the factory default values as well.
 * <p>
 *
 * @author Guenther Schreiner - Initial contribution
 */
public class VeluxThingConfiguration extends Configuration {

    public static final String THING_SCENE_NAME = VeluxBindingProperties.PROPERTY_SCENE_NAME;

    public String sceneName;

    /**
     * Default values - should not be modified
     */
    public VeluxThingConfiguration() {
        sceneName = "no-scene";
    }

}
